def map_syscall_to_subsystem(syscall_name):
    """Map syscall name to kernel subsystem."""
    if not syscall_name:
        return "kernel"
    if syscall_name.startswith("vm:"):
        return "mm"
    if syscall_name.startswith("disk:"):
        return "fs"
    if syscall_name.startswith("net:"):
        return "net"
    syscall_lower = syscall_name.lower()
    if any(x in syscall_lower for x in ["read", "write", "open", "close", "stat", "fsync"]):
        return "fs"
    if any(x in syscall_lower for x in ["socket", "connect", "send", "recv", "bind"]):
        return "net"
    if any(x in syscall_lower for x in ["mmap", "munmap", "brk", "mprotect"]):
        return "mm"
    if any(x in syscall_lower for x in ["clone", "fork", "exec", "wait", "exit"]):
        return "sched"
    return "kernel"


def map_interrupt_to_subsystem(interrupt_name):
    """Map interrupt name to kernel subsystem."""
    irq_lower = interrupt_name.lower()
    if "timer" in irq_lower:
        return "sched"
    if any(x in irq_lower for x in ["eth", "network", "wifi"]):
        return "net"
    if any(x in irq_lower for x in ["keyboard", "mouse", "usb"]):
        return "drivers"
    return "kernel"
